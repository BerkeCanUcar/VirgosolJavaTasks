package day1Tasks;

public class kareKup {

    public static void main(String[] args){

        int number = 4;

        System.out.println("Sayının kendisi = " + number);

        int kare = (number * number);

        System.out.println("Sayının karesi = " + kare);

        int kup = (number * number * number);

        System.out.println("Sayının küpü = " + kup);
    }
}

